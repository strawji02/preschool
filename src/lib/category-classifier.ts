/**
 * 룰 기반 분류 — 키워드 매칭으로 카테고리 결정
 * 매칭 실패 시 '가공·기타'로 fallback (보수적)
 */
export function classifyByRule(itemName: string, spec?: string): FoodCategory {
  const text = `${itemName ?? ''} ${spec ?? ''}`.toLowerCase()
  if (!text.trim()) return '가공·기타'

  for (const { category, keywords } of RULES) {
    for (const kw of keywords) {
      if (text.includes(kw.toLowerCase())) {
        return category
      }
    }
  }
  return '가공·기타'
}

/**
 * 같은 텍스트에 대해 여러 카테고리 키워드가 매칭되는지 확인 (디버깅용)
 */
export function classifyDebug(itemName: string, spec?: string): {
  category: FoodCategory
  matchedKeywords: { category: FoodCategory; keyword: string }[]
} {
  const text = `${itemName ?? ''} ${spec ?? ''}`.toLowerCase()
  const matches: { category: FoodCategory; keyword: string }[] = []
  for (const { category, keywords } of RULES) {
    for (const kw of keywords) {
      if (text.includes(kw.toLowerCase())) {
        matches.push({ category, keyword: kw })
      }
    }
  }
  return { category: classifyByRule(itemName, spec), matchedKeywords: matches }
}
